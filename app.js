let currentLanguage = localStorage.getItem("language") || "uz";
let activeSkillKey = null;

function applyTranslations(language) {
    const dictionary = translations[language] || translations.uz;

    document.documentElement.lang = language;
    document.title = dictionary.pageTitle;

    document.querySelectorAll("[data-i18n]").forEach(function (element) {
        const key = element.dataset.i18n;
        if (dictionary[key]) {
            element.innerHTML = dictionary[key];
        }
    });

    document.querySelectorAll("[data-placeholder]").forEach(function (element) {
        const key = element.dataset.placeholder;
        if (dictionary[key]) {
            element.placeholder = dictionary[key];
        }
    });

    const languageSelect = document.getElementById("languageSelect");
    if (languageSelect) {
        languageSelect.value = language;
    }

    if (activeSkillKey) {
        renderSkill(activeSkillKey);
    }
}

function renderSkill(key) {
    const area = document.getElementById("progress-area");
    const skill = skills[key];
    const dictionary = translations[currentLanguage] || translations.uz;

    area.innerHTML =
        '<div class="progress-wrap">' +
        '<div class="progress-label">' +
        '<span class="progress-name">' + skill.name + '</span>' +
        '<span class="progress-level">' + dictionary.skillLevel + ': ' + skill.pct + '%</span>' +
        '</div>' +
        '<div class="progress-track">' +
        '<div class="progress-fill" id="fill"></div>' +
        '</div>' +
        '<div class="progress-desc">' + dictionary.skillDescriptions[key] + '</div>' +
        '</div>';

    setTimeout(function () {
        const fill = document.getElementById("fill");
        if (fill) {
            fill.style.width = skill.pct + "%";
        }
    }, 30);
}

function showSkill(key, button) {
    const area = document.getElementById("progress-area");

    if (button.classList.contains("active")) {
        button.classList.remove("active");
        activeSkillKey = null;
        area.innerHTML = "";
        return;
    }

    document.querySelectorAll(".skill-btn").forEach(function (item) {
        item.classList.remove("active");
    });

    button.classList.add("active");
    activeSkillKey = key;
    renderSkill(key);
}

document.addEventListener("DOMContentLoaded", function () {
    const languageSelect = document.getElementById("languageSelect");

    applyTranslations(currentLanguage);

    if (languageSelect) {
        languageSelect.addEventListener("change", function (event) {
            currentLanguage = event.target.value;
            localStorage.setItem("language", currentLanguage);
            applyTranslations(currentLanguage);
        });
    }
});





