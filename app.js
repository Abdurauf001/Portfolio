document.addEventListener("DOMContentLoaded", function () {
    /* Default til = "en" */
    applyTranslations(currentLanguage);

    /* Til o'zgartirish */
    var languageSelect = document.getElementById("languageSelect");
    if (languageSelect) {
        languageSelect.addEventListener("change", function (event) {
            currentLanguage = event.target.value;
            applyTranslations(currentLanguage);
        });
    }

    /* Hamburger menu */
    var hamburger = document.getElementById("hamburger");
    var nav = document.getElementById("main-nav");
    if (hamburger && nav) {
        hamburger.addEventListener("click", function () {
            var open = nav.classList.toggle("open");
            hamburger.classList.toggle("open", open);
            hamburger.setAttribute("aria-expanded", open);
        });
        nav.querySelectorAll("a").forEach(function (link) {
            link.addEventListener("click", function () {
                nav.classList.remove("open");
                hamburger.classList.remove("open");
                hamburger.setAttribute("aria-expanded", "false");
            });
        });
    }

    /* Scroll — header shadow */
    window.addEventListener("scroll", function () {
        document.getElementById("site-header").classList.toggle("scrolled", window.scrollY > 10);
    });

    /* Skill cardlarni box-ga bosish imkonini beramiz */
    document.querySelectorAll(".skill-buttons .box").forEach(function (box) {
        box.style.cursor = "pointer";
        box.addEventListener("click", function () {
            var btn = box.querySelector(".skill-btn");
            if (btn) {
                var key = btn.getAttribute("onclick").match(/'(\w+)'/)[1];
                showSkill(key, btn);
                /* Active box border */
                document.querySelectorAll(".skill-buttons .box").forEach(function (b) {
                    b.style.borderColor = "";
                });
                if (activeSkillKey) box.style.borderColor = "var(--blue)";
            }
        });
    });

    /* Form submit */
    var form = document.getElementById("form");
    if (form) {
        form.addEventListener("submit", async function (e) {
            e.preventDefault();
            var name = document.getElementById("name").value.trim();
            var email = document.getElementById("email").value.trim();
            var phone = document.getElementById("number").value.trim();
            var msg = document.getElementById("msg").value.trim();
            var button = form.querySelector('button[type="submit"]');
            var status = document.getElementById("form-status");

            var token = "8036920676:AAHkQiIfOr7Bc_Y_lq7Y09OohKhEJ54Mi2g";
            var chatId = "6148285981";
            var text =
                "Yangi xabar!\n\n" +
                "Portfolio_2\n" +
                "Ism: " + name + "\n" +
                "Email: " + email + "\n" +
                "Telefon: " + phone + "\n" +
                "Xabar: " + msg;

            if (!name || !email || !phone || !msg) {
                status.textContent = "Iltimos, barcha maydonlarni to'ldiring.";
                status.className = "form-status error";
                return;
            }

            if (chatId === "YOUR_CHAT_ID") {
                status.textContent = "Telegram chat_id kiritilmagan.";
                status.className = "form-status error";
                return;
            }

            button.disabled = true;
            button.textContent = "Yuborilmoqda...";
            status.textContent = "";
            status.className = "form-status";

            try {
                var response = await fetch("https://api.telegram.org/bot" + token + "/sendMessage", {
                    method: "POST",
                    headers: { "Content-Type": "application/json" },
                    body: JSON.stringify({
                        chat_id: chatId,
                        text: text
                    })
                });
                var result = await response.json();

                if (!response.ok || !result.ok) {
                    throw new Error(result.description || "Adminga yuborib bo'lmadi.");
                }

                form.reset();
                status.textContent = "Xabar adminga yuborildi.";
                status.className = "form-status success";
            } catch (error) {
                status.textContent = "Xabar yuborilmadi. Keyinroq qayta urinib ko'ring.";
                status.className = "form-status error";
            } finally {
                button.disabled = false;
                button.textContent = (translations[currentLanguage] || translations.en).sendButton;
            }
        });
    }
});
